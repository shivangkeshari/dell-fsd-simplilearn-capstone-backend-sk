package com.icinbank.details;

public class TransactionDetails {

	private long acct;
	private int amt;
	
	
	public long getAcct() {
		return acct;
	}
	public void setAcct(long acct) {
		this.acct = acct;
	}
	public int getAmt() {
		return amt;
	}
	public void setAmt(int amt) {
		this.amt = amt;
	}
}
